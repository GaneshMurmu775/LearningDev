let arr2 = [1, 7.2, 'a', "Ganesh Murmu", true];

//Push Methode
console.log("arr2 before push: " + arr2);

arr2.push("New Item");
console.log("arr2 after push: " + arr2);

//Pop Methode
console.log("arr2 before pop: " + arr2);

arr2.pop("New Item");
console.log("arr2 after pop: " + arr2); 

//shift methode
console.log("arr2 before shift: " + arr2);

arr2.shift();
console.log("arr2 after shift: " + arr2);

//Unshift methode
console.log("arr2 before unshift: " + arr2);

arr2.unshift("Mike");
console.log("arr2 after unshift: " + arr2);

